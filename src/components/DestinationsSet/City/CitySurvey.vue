            <h2>美食概况</h2>
            <p>瓦拉纳西的美食很多，都藏在小巷子里，只是因为这里的环境确实比较糟糕，肠胃不好的人就不要吃路边小吃了，最好去酒店的餐厅，比较干净。<br>
              用餐礼仪<br>
              印度人习惯用手直接抓取食物，如果你不能接受也没关系，一般的餐厅都会配备刀叉。要注意吃东西不能使用左手，一定要使用右手，因为当地人认为左手是不洁的。<br>
              如果前往当地人家里做客，最好不要在盘子里剩下食物。</p>
            <h4>炒饭及套餐</h4>
