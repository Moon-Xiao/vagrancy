            <h2>最佳旅行时间</h2>
            <p>
              坐落在北印度的瓦拉纳西属典型的热带季风气候。一年分为三个季节：凉季、热季和雨季。最佳旅行季节是10月-次年3月为凉季，日间在气候宜人，节日密集，大多数当地人会在这段期间举行婚礼，但夜间最低气温会达到5摄氏度，比较阴冷，有些宾馆和民宅内也无法提供热水。4-6月为热季，非常不推荐这段时间游览瓦拉纳西，这段时间日间最高温会达到40-50摄氏度，且电力供应不稳定。7-9月是雨季，道路会出现大片积水，交通拥堵，非常不利于出行。</p>
