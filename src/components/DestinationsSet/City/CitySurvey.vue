          <div class="part" id="wiki_part_5"><h2>货币</h2>
            <p>
              印度的货币为印度卢比（INR，符号“₹”）。印度的金融系统与欧美接轨，Mastercard、Visa信用卡在大城市消费和网上交易时里很好用，但人民币在印度几乎不流通，印度卢比也无法在中国国内兑换，游客需从国内携带美元现金以兑换卢比。在瓦拉纳西，中国的银联卡几乎无法使用，还是建议多备些现金。也可以通过西联汇款（West Union Pay）从中国进行即时到账的国际汇款。</p>
