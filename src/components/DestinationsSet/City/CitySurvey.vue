            <p>
              印度并不流行给小费的文化，但一些西方游客给小费习惯却或多或少的影响了当地旅游从业人员。如果遇到帮你提行李的人，可按20卢比/件的标准支付小费，如果不希望支付小费请谢绝他们的“帮助”，特别是在机场、火车站、长途车站这样的地方。在旅游餐厅或者酒店里，结账时会被收取5%-10%的服务费，已含在账单里，所以不是必须给小费。也不必给电动三轮车司机小费。如果使用了包车服务，觉得司机服务的好，可以按200卢比/天支付小费。如果身上没有带零钱，一些司机不会找给你零钱，以此收取小费，所以记得准备零钱。一些人还会打着“停车费”、“高温费”、“买水钱”的名号索要小费，如果不愿意支付，态度可以根据情况适当强硬一些。</p>
